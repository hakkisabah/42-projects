#include "push_swap.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	exit_to_error(void)
{
	ft_putstr_fd("Error\n", 1);
	exit (1);
}
